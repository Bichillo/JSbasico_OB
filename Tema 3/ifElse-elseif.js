"use strict";

//if else + if else

let nota = 5;

if (nota === 5) {
  console.log("Enhorabuena has aprobado");
} else if (nota === 4) {
  console.log("Has suspendido, podrias haberlo hecho mejor");
} else if (nota === 3) {
  console.log("Necesitas mejorar");
} else if (nota === 2) {
  console.log("Esfuerzate mas");
} else if (nota === 1) {
  console.log("No has estudiado nada");
} else {
  console.log("ERROR, introduce unaa nota entre el 1 y el 5");
}
